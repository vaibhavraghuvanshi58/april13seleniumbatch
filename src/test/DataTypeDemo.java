package test;

public class DataTypeDemo {
public static void main(String[] args) {
	// casting
	byte b1 = (byte)260;
	System.out.println(b1);
	// byte, short , int, long = numerical data type
	
	int i = 0X4a; // hexa decimal // octal
	System.out.println(i);
	
	long l = 434_768_367_43l;
	
	float fl = 3.4f;
	double d2 = 9.8d;
	char ch = 'a';
	boolean bol = true;
	
}
}
